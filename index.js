function chunkArray(ary) {
  const tempAry = []
  const MAX_COLUMN = 8
  for (let i = 0; i < ary.length; i += MAX_COLUMN) {
    tempAry.push(ary.slice(i, i + MAX_COLUMN))
  }
  if (tempAry[0].length < MAX_COLUMN) {
    tempAry[0].push(...Array(MAX_COLUMN - tempAry[0].length).fill({}))
  }
  return tempAry
}

function renderCases (cases) {
  return Object.keys(cases).map(key => {
    return `
      <h2>${key}</h2>
      <table
        border="0"
        cellpadding="0"
        cellspacing="0"
      >
      <tbody>
        ${renderTr(cases[key])}
      </tbody>
      </table>
    `
  }).join('')
}

function renderTr (arr) {
  return chunkArray(arr).map(a => {
    return `
      <tr>
        ${a.map(o => {
          return `
            <td align="center">
              <img class="${o.url ? '' : 'hidden'}" height="80" width="80" src="${o.url}" />
              <p class="${o.url ? '' : 'hidden'}">${o.name}</p>
            </td>
          `
        }).join('')}
      </tr>
    `
  }).join('')
}

document.getElementById("app").innerHTML = renderCases(userCases)